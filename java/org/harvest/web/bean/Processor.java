package org.harvest.web.bean;

/**
 * 处理器类
 * @author jone.yuan
 *
 */
public class Processor {

	
	/**
	 * 处理器类名
	 */
	private String proc_class;
	
	/**
	 * 处理器名称
	 */
	private String proc_name;
	
	/**
	 * 处理器描述
	 */
	private String proc_descr;
	
	
	
	public String getProc_class() {
		return proc_class;
	}
	public void setProc_class(String proc_class) {
		this.proc_class = proc_class;
	}
	public String getProc_name() {
		return proc_name;
	}
	public void setProc_name(String proc_name) {
		this.proc_name = proc_name;
	}
	public String getProc_descr() {
		return proc_descr;
	}
	public void setProc_descr(String proc_descr) {
		this.proc_descr = proc_descr;
	}

	
	
}
